    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        ComboItem sItem = (ComboItem)jComboBox3.getSelectedItem();
        String table;
        if(sItem.getId().startsWith("L")){
            table = "laptop";
        }
        else {
            table = "accessory";
        }
        DBConnection dBConnection = new DBConnection();
        ResultSet resultSet = dBConnection.getItem(table, sItem.getId());
        Item item = new Item();
        try {
            resultSet.next();       
            item.setItemNumber(resultSet.getString("itemnumber"));
            item.setModel(resultSet.getString("model"));
            item.setBrand(resultSet.getString("brand"));
            item.setPrice(resultSet.getDouble("price"));
            jButton2.setEnabled(true);
        } catch (SQLException ex) {
            Logger.getLogger(Purchase.class.getName()).log(Level.SEVERE, null, ex);
        }        
        cartItems[index] = item;        
        
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        double totalPrice = 0;
        for(int i = 0; i <= index; i++){
            Object rowData[] = new Object[3];
            rowData[0] = cartItems[i].getItemNumber();
            rowData[1] = cartItems[i].getBrand() + " " + cartItems[i].getModel();
            rowData[2] = cartItems[i].getPrice();
            totalPrice = totalPrice + cartItems[i].getPrice();
            model.addRow(rowData);
        }
        jLabel6.setText(String.valueOf(totalPrice));
        jLabel13.setText(String.valueOf(totalPrice));
        cart.setItems(cartItems);
        index ++;
      
    }//GEN-LAST:event_jButton1ActionPerformed

