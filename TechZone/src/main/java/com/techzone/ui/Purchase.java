    private void jComboBox4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox4ActionPerformed
        // TODO add your handling code here:
        ComboItem sItem = (ComboItem)jComboBox4.getSelectedItem();
        if (sItem != null){
            DBConnection dBConnection = new DBConnection();  
            ResultSet resultSet = dBConnection.getUser("customer", sItem.getId());
            Customer customer = new Customer();
            try {
                resultSet.next();       
                customer.setContactnumber(resultSet.getString("contactnumber"));
                customer.setCustomerid(resultSet.getString("customerid"));
                customer.setFirstname(resultSet.getString("firstname"));
                customer.setLastname(resultSet.getString("lastname"));
                customer.setEmail(resultSet.getString("email"));

            } catch (SQLException ex) {
                Logger.getLogger(Purchase.class.getName()).log(Level.SEVERE, null, ex);
            }

            jLabel8.setText(customer.getFirstname() + " " + customer.getLastname());
            jLabel9.setText(customer.getContactnumber());
            jLabel10.setText(customer.getEmail());

            cart.setCustomer(customer);
        }
    }//GEN-LAST:event_jComboBox4ActionPerformed

